#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	tmp;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = n * -1;
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		tmp = n % 10 + 48;
		write (fd, &tmp, 1);
	}
	if (n <= 9)
	{
		tmp = n % 10 + 48;
		write(fd, &tmp, 1);
	}
}
